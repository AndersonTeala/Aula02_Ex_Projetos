int main(){
    int n;

    do{
        printf("\n Digite o valor de n \n");
        scanf("%f", &n);

    }while((n<=1,3,5,7,9) || (n>=2,4,6,8,10));

    switch(n){
        case 1:
            if(n = 2,4,6,8,10);
                n = n/2;
            printf("\n resultado de n é par: %2f\n", n);
        break;

        case 2:
            if(n = 1,3,5,7,9);
                n = n*3+1;
            printf("\n resultado de n é par: %2f\n", n);
        break;
    }

    return 0;
}